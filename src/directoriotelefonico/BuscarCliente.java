    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        
        String telefono = jtfTelefono.getText();
        if(buscarCliente(telefono)==null){
            JOptionPane.showMessageDialog(this, "El cliente no existe");
        }else{
        jtfDni.setText(buscarCliente(telefono).getDni());
        jtfApellido.setText(buscarCliente(telefono).getApellido());
        jtfNombre.setText(buscarCliente(telefono).getNombre());
        jtfCiudad.setText(buscarCliente(telefono).getCiudad());
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

